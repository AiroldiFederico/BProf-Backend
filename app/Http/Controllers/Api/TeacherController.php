    {
        $teachers = Teacher::with('subjects')->get();

        if ($request->has('subject_id')) {
            $teachers = Teacher::with('subjects')->whereHas('subjects', function($q) use ($request){
                $q->where('subject_id', '=', $request->subject_id);
            })->get();
        }

        return response()->json([
            'success' => true,
            'results' => $teachers
        ]);
    }

